# Arithmetic Operators:
# 1. Addition (+): Adds two operands.
# 2. Subtraction (-): Subtracts the second operand from the first.
# 3. Multiplication (*): Multiplies two operands.
# 4. Division (/): Divides the first operand by the second.
# 5. Modulus (%): Returns the remainder of the division of the first operand by the second
# 6. Exponentiation (**): Raises the first operand to the power of the second.
# 7. Floor Division (//): Divides the first operand by the second and returns the largest integer less than or equal to the result.

# Comparison Operators:
# 1. Equal to (==): Returns True if the operands are equal.
# 2. Not equal to (!=): Returns True if the operands are not equal.
# 3. Greater than (>): Returns True if the left operand is greater than the right operand.
# 4. Less than (<): Returns True if the left operand is less than the right operand.
# 5. Greater than or equal to (>=): Returns True if the left operand is greater than or equal to the right operand.
# 6. Less than or equal to (<=): Returns True if the left operand is less than or equal to the right operand.

# Logical Operators:
# 1. And (and): Returns True if both operands are true.
# 2. Or (or): Returns True if at least one of the operands is true.
# 3. Not (not): Returns True if the operand is false, and vice versa.

# Assignment Operators:
# 1. Assignment (=): Assigns a value to a variable.
# 2. Add and assign (+=): Adds the right operand to the left operand and assigns the result to the left operand.
# 3. Subtract and assign (-=): Subtracts the right operand from the left operand and assigns the result to the left operand.
# 4. Multiply and assign (*=): Multiplies the left operand by the right operand and assigns the result to the left operand.
# 5. Divide and assign (/=): Divides the left operand by the right operand and assigns the result to the left operand.
# 6. Modulus and assign (%=): Returns the remainder of the division of the left operand by the right operand and assigns the result to the left operand.
# 7. Exponentiation and assign (**=): Raises the left operand to the power of the right operand and assigns the result to the left operand.
# 8. Floor Division and assign (//=): Divides the left operand by the right operand and returns the largest integer less than or equal to the result, then assigns this value to the left operand.