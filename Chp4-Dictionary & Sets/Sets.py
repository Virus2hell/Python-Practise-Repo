sets = {1, 2, 3, 4, 5, 5, 2, 1} # This will create a set with the values 1, 2, 3, 4, and 5. Note that sets do not allow duplicate values, so the resulting set will only contain unique values.

print(sets)

e = set() # This will create an empty set. Note that we cannot create an empty set using empty curly braces {} because that will create an empty dictionary instead.
print(e)

s = set([1, 2, 3, 4, 5]) # This will create a set from the list [1, 2, 3, 4, 5] and assign it to the variable s.
print(s)

s1 = set("Hello") # This will create a set from the string "Hello" and assign it to the variable s1. Note that sets do not allow duplicate values, so the resulting set will only contain unique characters from the string.
print(s1)

