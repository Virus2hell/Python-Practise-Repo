# While loop

i = 0
while i < 5:
    print(i)
    i += 1  # This will increment the value of i by 1 in each iteration of the loop, so that the loop will eventually terminate when i becomes equal to 5.

j = 1
while j <= 10:
    print(j)
    j += 1  # This will increment the value of j by 1 in each iteration of the loop, so that the loop will eventually terminate when j becomes equal to 11.

k = 1
while k < 10:
    print(k)
    k += 2  # This will increment the value of k by 2 in each iteration of the loop, so that the loop will eventually terminate when k becomes equal to 11.

l = 10
while l > 0:
    print(l)
    l -= 1  # This will decrement the value of l by 1 in each iteration of the loop, so that the loop will eventually terminate when l becomes equal to 0.

