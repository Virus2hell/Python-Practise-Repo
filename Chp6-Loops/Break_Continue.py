# break continue

for i in range(1, 11):
    if i == 5:
        break  # This will exit the loop when i becomes equal to 5
    print(i)


for i in range(1, 11):
    if i == 5:
        continue  # This will skip the rest of the code in the loop when i becomes equal to 5 and move on to the next iteration of the loop
    print(i)

