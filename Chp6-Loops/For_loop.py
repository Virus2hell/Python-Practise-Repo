# for loop

for i in range(5):  # This will print the numbers from 0 to 4 (5 is exclusive)
    print(i)

for i in range(1, 6):  # This will print the numbers from 1 to 5 (6 is exclusive)
    print(i)

for i in range(1, 10, 2):  # This will print the odd numbers from 1 to 9 (10 is exclusive)
    print(i)

for i in range(10, 0, -1):  # This will print the numbers from 10 to 1 (0 is exclusive) in reverse order
    print(i)

l = ["apple", "banana", "cherry"]
for item in l:
    print(item)

t = ("apple", "banana", "cherry")
for i in t:
    print(i)
